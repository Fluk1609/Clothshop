    private void txtfnameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtfnameKeyReleased
        // TODO add your handling code here:
        con = ClothPrOject.getConnection();
        try {
            String sql = "SELECT * FROM `useid`";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                String name = rs.getString("uNAME");
                if(name.equalsIgnoreCase(txtfname.getText())){
                    JOptionPane.showMessageDialog(this, "รหัสซ้ำ กรุณาเปลี่ยนรหัส","แจ้งเตือน",JOptionPane.WARNING_MESSAGE);
                }
            }
        } catch(Exception ex){
            System.out.print("Not connection");
        }
    }//GEN-LAST:event_txtfnameKeyReleased

