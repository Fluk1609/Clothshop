    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String fname = txtfname.getText();
        String passw = txtpassw.getText();
        String sex = "";
        if(rdoMale.isSelected())
        {
            sex = rdoMale.getText();
            
        }
        if(rdoFemale.isSelected())
        {
            sex = rdoFemale.getText();
        }
        String add = txtadd.getText();
        con = ClothPrOject.getConnection();
        try{
            pst = con.prepareStatement("INSERT INTO useid( uNAME, uPass, address, sex) VALUES (?,?,?,?)");
            pst.setString(1, fname);
            pst.setString(2, passw);
            pst.setString(4, sex);
            pst.setString(3, add);
            int ok = JOptionPane.showConfirmDialog(rootPane, "Confirm","Are you Sure",JOptionPane.YES_NO_OPTION);
            if(ok == 0){
                pst.execute();
                JOptionPane.showMessageDialog(rootPane, "Sucessfullt","Done...",1);
            }
        }catch(Exception ex){
            System.out.print("Not connection");
        }

    }//GEN-LAST:event_jButton1ActionPerformed
